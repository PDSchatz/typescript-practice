// Run-Length Encoding Algorithm in TypeScript
// Task: Implement the run-length encoding algorithm,
// a simple form of data compression where consecutive duplicates of elements are encoded as tuples [N, E], 
// where N is the number of duplicates of the element E.

// Your TypeScript function should take a string as input and return an array of tuples. Each tuple contains a number and a character, 
// representing how many times consecutively the character appears in the input string.
// TypeScript Example:
// encode("aaaabccaadeeee"); -> [[4, "a"], [1, "b"], [2, "c"], [2, "a"], [1, "d"], [4, "e"]]

// export function encode.... Good Luck!

export function encode(str: string): (string|number)[][] {
    const strArr = str.match(/(.)\1*/g)
    if(strArr){
        return strArr.map(subStr => {
            return [subStr.length, subStr[0]]
        })
    } else {
        return []
    }
}